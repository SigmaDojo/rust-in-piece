extern crate bowling_kata;
use bowling_kata::Game;

const IMPLEMENT_ME: i32 = -1;

#[test]
fn gutter_game() {
    let result = IMPLEMENT_ME;
    assert_eq!(IMPLEMENT_ME, 0);
}

#[test]
fn all_ones() {
    let result = IMPLEMENT_ME;
    assert_eq!(IMPLEMENT_ME, 20);
}

#[test]
fn one_spare_followed_by_3_and_4() {
    let result = IMPLEMENT_ME;
    assert_eq!(IMPLEMENT_ME, 20);
}

#[test]
fn perfect_game() {
    let result = IMPLEMENT_ME;
    assert_eq!(IMPLEMENT_ME, 300);
}
